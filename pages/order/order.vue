<script>
	import uniLoadMore from '@/components/uni-load-more/uni-load-more.vue';
	import empty from "@/components/empty";
	import Json from '@/Json';
	import {
		mapState,
		mapMutations
	} from 'vuex';
	export default {
		components: {
			uniLoadMore,
			empty
		},
		data() {
			return {
				orderList: [],
				pageNo: 1,
				pageSize: 10,
				tabCurrentIndex: 0,
				loadingType: '',
				navList: [{
						state: '',
						text: this.$i18nMsg().index.navList.all,
						loadingType: 'more',
						orderList: []
					},
					{
						state: '0',
						text: this.$i18nMsg().index.navList.state0,
						loadingType: 'more',
						orderList: []
					},
					{
						state: '1',
						text: this.$i18nMsg().index.navList.state1,
						loadingType: 'more',
						orderList: []
					},
					{
						state: '2',
						text: this.$i18nMsg().index.navList.state2,
						loadingType: 'more',
						orderList: []
					},
					{
						state: '3',
						text: this.$i18nMsg().index.navList.state3,
						loadingType: 'more',
						orderList: []
					}
				],
			};
		},

		onLoad(options) {
			uni.setNavigationBarTitle({
				title: this.orderMsg.title
			})
			this.tabCurrentIndex = Number(options.state);
			this.searchOrder(this.translateTabIndex(this.tabCurrentIndex));
		},
		//下拉刷新
		onPullDownRefresh() {
			//重新加载数据
			this.resetPage();
			this.searchOrder(this.translateTabIndex(this.tabCurrentIndex));
		},
		computed: {
			i18n() {
				return this.$i18nMsg().index
			},
			orderMsg() {
				return this.$i18nMsg().index.order
			},
			...mapState(['hasLogin', 'merchantInfo', 'footPrint'])
		},
		methods: {
			//加载更多
			loadMore() {
				if (this.loadingType === 'more') {
					this.pageNo = this.pageNo + 1;
					this.searchOrder(this.translateTabIndex(this.tabCurrentIndex))
				}
			},
			searchOrder(orderStatus) {
				let that = this;
				let keyArray = ['MERCHANT'];
				let searchOptions = {
					merchantUuid: this.merchantInfo.merchantUuid,
					startIndex: (this.pageNo - 1) * this.pageSize,
					pageSize: this.pageSize
				};
				if (orderStatus) {
					keyArray.push('ORDERSTATUS');
					searchOptions.orderStatus = orderStatus;
				}
				searchOptions.keyArray = keyArray;
				this.loadingType = 'loading';
				this.$api.request.searchOrder(searchOptions, res => {
					if (res.body.status.statusCode === '0') {
						var orders = res.body.data.orders;
						var total = res.body.data.total;
						var orderList = this.orderList;
						orders.forEach(function(val, index) {
							var orderStatusTran = that.$api.util.translateOrderStatus(val.orderStatus);
							val.orderStatusDesc = orderStatusTran.orderStatusDesc;
							val.orderStatusColor = orderStatusTran.orderStatusColor;
							orderList.push(val);
						})
						this.orderList = orderList;
						if (this.orderList.length >= total) {
							this.loadingType = 'noMore';
						} else {
							this.loadingType = 'more';
						}
					} else {
						console.log(res.body.status.errorDesc);
					}
				}, true);
			},
			translateTabIndex(tabIndex) {
				let orderStatus = '';
				switch (tabIndex) {
					case 0:
						orderStatus = '';
						break;
					case 1:
						orderStatus = '0';
						break;
					case 2:
						orderStatus = '1';
						break;
					case 3:
						orderStatus = '2';
						break;
					case 4:
						orderStatus = '3';
						break;
				}
				return orderStatus;
			},
			//swiper 切换
			changeTab(e) {
				this.tabCurrentIndex = e.target.current;
				this.resetPage();
				this.searchOrder(this.translateTabIndex(this.tabCurrentIndex))
			},
			//顶部tab点击
			tabClick(index) {
				this.tabCurrentIndex = index;
			},
			//删除订单
			deleteOrder(item) {
				let that = this;
				uni.showModal({
					content:  that.orderMsg.deleteCon,
					success: (e) => {
						if (e.confirm) {
							this.$api.request.delOrder({
								actionType: 'DELETE',
								orderNo: item.orderNo
							}, res => {
								if (res.body.status.statusCode === '0') {
									that.$api.msg(that.orderMsg.deleteOrder);
									that.resetPage();
									that.searchOrder(that.translateTabIndex(that.tabCurrentIndex));
								} else {
									that.$api.msg(res.body.status.errorDesc);
								}
							}, false);
						}
					}
				})
			},
			resetPage() {
				this.pageNo = 1;
				this.orderList = [];
			},
			//取消订单
			cancelOrder(item) {
				let that = this;
				var content = that.orderMsg.cancelCon;
				uni.showModal({
					content: content,
					success: (e) => {
						if (e.confirm) {
							this.$api.request.cancelOrder({
								orderNo: item.orderNo
							}, res => {
								if (res.body.status.statusCode === '0') {
									that.$api.msg(that.orderMsg.cancelOrderSuccess);
									that.resetPage();
									that.searchOrder(that.translateTabIndex(that.tabCurrentIndex));
								} else {
									that.$api.msg(res.body.status.errorDesc);
								}
							}, false);
						}
					}
				})
			},
			pay(item) {
				uni.navigateTo({
					url: '/pages/money/pay?orderNo=' + item.orderNo
				})
			},
			deliver(item) {
				uni.navigateTo({
					url: '/pages/order/deliverOrder?orderNo='+item.orderNo
				})
			},
			evaluate(item) {
				uni.navigateTo({
					url: '/pages/order/evaluate?orderNo=' + item.orderNo
				})
			},
			navProductDetail(item){
				uni.navigateTo({
					url: '/pages/product/product?id='+item.productDTO.productUuid
				})
			},
			applyAfterSale(item){
				uni.showModal({
					content: that.orderMsg.applyAfterSale,
					success: (e) => {
						if (e.confirm) {
							uni.navigateTo({
								url: '/pages/aftersale/index?orderNo='+item.orderNo
							})
						}
					}
				})
			},
			viewAfterSale(item){
				uni.navigateTo({
					url: '/pages/aftersale/detail?id='+item.afterSaleNo
				})
			},
			viewOrder(item){
				uni.navigateTo({
					url: '/pages/order/detail?orderNo='+item.orderNo
				})
			}
		},
	}
</script>

